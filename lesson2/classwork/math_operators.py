print(4 + 3) # 7 (Addition)
print(4 - 3) # 1 (Subtraction)
print(4 * 3) # 12 (Multiplication)
print(4 / 3) # 1.333 (Division)
print(4 // 3) # divides then round down 4 // 3 = 1 (Floor Division)
print(4 % 3) # 4 / 3 = 1 + 1/3. Which means 1 is the remainder. 1 (Modulus)
print(4 ** 3) # print 2 to the power of 3 which is 8 (Exponentiation)

print(2 + 3 * 4) # 14
print((2 + 3) * 4) # 20

